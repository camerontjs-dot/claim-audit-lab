def test_public_example_files_avoid_private_data_and_secret_markers() -> None:
    """Public draft, evidence, and report files stay fictional or sanitized."""
    public_files = [
        path for directory in PUBLIC_EXAMPLE_DIRS for path in directory.rglob("*") if path.is_file()
    ]

    assert public_files
    for path in public_files:
        text = path.read_text(encoding="utf-8")
        for pattern in PUBLIC_DATA_SENSITIVE_PATTERNS:
            assert re.search(pattern, text, flags=re.IGNORECASE) is None, (
                f"{path.relative_to(PROJECT_ROOT)} matched sensitive-data pattern {pattern!r}"
            )


