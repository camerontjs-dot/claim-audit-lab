def test_json_report_round_trips_to_audit_report_without_null_values() -> None:
    """JSON output remains tied to the typed report contract."""
    rendered = render_json_report(_ai_research_report())
    parsed = AuditReport.model_validate(json.loads(rendered))

    assert '"document_id": "ai-research-note"' in rendered
    assert "null" not in rendered
    assert parsed.summary.total_claims == 4
    assert parsed.claims[0].claim.id.startswith("claim-")


